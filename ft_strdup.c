#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		length;
	char	*dup;
	int		i;

	length = ft_strlen(s);
	dup = (char *)malloc(sizeof(char) * (length + 1));
	if (!dup)
		return (NULL);
	i = 0;
	while (s[i])
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
